    private void PnlBtnIniciarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PnlBtnIniciarMouseClicked
        String usuario = txtUsuario.getText();
        String contraseña = new String(TxtContraseña.getPassword());

        Conexion conexion = new Conexion();

        if (conexion.validarCredenciales(usuario, contraseña)) {
            // Credenciales válidas, abrir el formulario FrmPrincipal
            FrmPrincipal frmPrincipal = new FrmPrincipal();
            frmPrincipal.setVisible(true);

            // Cierra el formulario actual (FrmLogin)
            this.dispose();
        } else {
            // Credenciales inválidas, muestra un mensaje de error
            JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos", "Error de inicio de sesión", JOptionPane.ERROR_MESSAGE);
        }

        // Cierra la conexión después de usarla
        conexion.close();

    }//GEN-LAST:event_PnlBtnIniciarMouseClicked

